


for (i = 0; i <= 100; i++) {

    if (i >= 60 && i < 70) {
        console.log('For' + i + ', you got a D')
    }
    else if (i >= 70 && i < 80) {
        console.log('For' + i + ', you got a C')
    }
    else if (i >= 80 && i < 90) {
        console.log('For' + i + ', you got a B')
    }
    else if (i >= 90) {
        console.log('For' + i + ', you got a A!')
    }
}

